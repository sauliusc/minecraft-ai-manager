#!/usr/bin/env bash
# setup.sh вЂ” Interactive first-time configuration wizard.
# Run once before the first `make deploy`.
set -euo pipefail

# в”Ђв”Ђ Colours в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
GREEN='\033[0;32m'; YELLOW='\033[1;33m'; RED='\033[0;31m'
CYAN='\033[0;36m'; BOLD='\033[1m'; RESET='\033[0m'

ok()   { echo -e "  ${GREEN}вњ“${RESET} $*"; }
ask()  { echo -e "  ${YELLOW}?${RESET} $*"; }
err()  { echo -e "  ${RED}вњ—${RESET} $*" >&2; }
info() { echo -e "  ${CYAN}в„№${RESET} $*"; }

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
DEPLOY_DIR="$(dirname "$SCRIPT_DIR")"
ENV_FILE="$DEPLOY_DIR/.env"

echo ""
echo -e "${BOLD}в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ${RESET}"
echo -e "${BOLD}   Minecraft AI Manager вЂ” Setup Wizard          ${RESET}"
echo -e "${BOLD}в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ${RESET}"
echo ""

# в”Ђв”Ђ Prerequisite checks в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo -e "${BOLD}Checking prerequisites...${RESET}"

if ! command -v docker &>/dev/null; then
  err "Docker not found. Install it from https://docs.docker.com/engine/install/"
  exit 1
fi
ok "Docker $(docker --version 2>/dev/null | awk '{print $3}' | tr -d ',' || echo 'installed')"

if ! docker compose version &>/dev/null; then
  err "Docker Compose plugin not found. Run: sudo apt install docker-compose-plugin"
  exit 1
fi
ok "Docker Compose $(docker compose version 2>/dev/null | grep -oE '[0-9]+\.[0-9]+\.[0-9]+' | head -1 || echo 'installed')"

if [[ -f "$ENV_FILE" ]]; then
  ask ".env already exists. Overwrite? [y/N]"
  read -r answer
  if [[ ! "$answer" =~ ^[Yy]$ ]]; then
    info "Keeping existing .env. Skipping setup."
    exit 0
  fi
fi

echo ""
echo -e "${BOLD}Let's configure your deployment:${RESET}"
echo -e "  ${CYAN}(Press Enter to accept the default shown in brackets)${RESET}"
echo ""

# в”Ђв”Ђ Collect user inputs в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
ask "Web panel port [80]:"
read -r HTTP_PORT
HTTP_PORT="${HTTP_PORT:-80}"

echo ""
echo -e "${BOLD}Admin account (web panel login):${RESET}"
ask "Admin email:"
read -r ADMIN_EMAIL
while [[ -z "$ADMIN_EMAIL" ]]; do
  err "Admin email cannot be empty."
  ask "Admin email:"
  read -r ADMIN_EMAIL
done

ask "Admin password (min 12 chars):"
read -rs ADMIN_PASSWORD || true
echo ""
while [[ ${#ADMIN_PASSWORD} -lt 12 ]]; do
  err "Password must be at least 12 characters."
  ask "Admin password:"
  read -rs ADMIN_PASSWORD || true
  echo ""
done

echo ""
echo -e "${BOLD}Minecraft RCON (server console access):${RESET}"
ask "RCON port [25575]:"
read -r RCON_PORT
RCON_PORT="${RCON_PORT:-25575}"

ask "RCON password (auto-generate? press Enter):"
read -rs RCON_PASSWORD_INPUT || true
echo ""

echo ""
echo -e "${BOLD}MCP server (Claude AI tool access):${RESET}"
ask "MCP port [3100]:"
read -r MCP_PORT
MCP_PORT="${MCP_PORT:-3100}"

ask "MCP auth token (auto-generate? press Enter):"
read -rs MCP_AUTH_TOKEN_INPUT || true
echo ""

echo ""
echo -e "${BOLD}Generating secrets...${RESET}"

gen_secret() { openssl rand -hex "$1" 2>/dev/null || head -c "$1" /dev/urandom | xxd -p | tr -d '\n' | head -c $(( $1 * 2 )); }

POSTGRES_PASSWORD=$(gen_secret 20)
REDIS_PASSWORD=$(gen_secret 20)
JWT_SECRET=$(gen_secret 32)
JWT_REFRESH_SECRET=$(gen_secret 32)
BRIDGE_SECRET=$(gen_secret 16)
RCON_PASSWORD="${RCON_PASSWORD_INPUT:-$(gen_secret 16)}"
MCP_AUTH_TOKEN="${MCP_AUTH_TOKEN_INPUT:-$(gen_secret 20)}"

ok "POSTGRES_PASSWORD"
ok "REDIS_PASSWORD"
ok "JWT_SECRET"
ok "JWT_REFRESH_SECRET"
ok "BRIDGE_SECRET"
ok "RCON_PASSWORD"
ok "MCP_AUTH_TOKEN"

# в”Ђв”Ђ Write .env в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
cat > "$ENV_FILE" <<EOF
# Generated by setup.sh on $(date -u +"%Y-%m-%d %H:%M UTC")
# Do NOT commit this file to git.

# в”Ђв”Ђ Server в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
HTTP_PORT=${HTTP_PORT}

# в”Ђв”Ђ Database в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
POSTGRES_DB=craftcontrol
POSTGRES_USER=craftcontrol
POSTGRES_PASSWORD=${POSTGRES_PASSWORD}

# в”Ђв”Ђ Redis в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
REDIS_PASSWORD=${REDIS_PASSWORD}

# в”Ђв”Ђ JWT в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
JWT_SECRET=${JWT_SECRET}
JWT_REFRESH_SECRET=${JWT_REFRESH_SECRET}
JWT_EXPIRES_IN=15m
JWT_REFRESH_EXPIRES_IN=7d

# в”Ђв”Ђ Plugin bridge в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
BRIDGE_SECRET=${BRIDGE_SECRET}

# в”Ђв”Ђ Minecraft server в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
MINECRAFT_VERSION=1.21.1
MINECRAFT_MEMORY=4G

# в”Ђв”Ђ Minecraft RCON в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
RCON_PORT=${RCON_PORT}
RCON_PASSWORD=${RCON_PASSWORD}

# в”Ђв”Ђ Admin account в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
ADMIN_EMAIL=${ADMIN_EMAIL}
ADMIN_PASSWORD=${ADMIN_PASSWORD}

# в”Ђв”Ђ MCP server (Claude AI tool access) в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
MCP_PORT=${MCP_PORT}
MCP_AUTH_TOKEN=${MCP_AUTH_TOKEN}

# в”Ђв”Ђ Docker image tag (set automatically by deploy.sh) в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
IMAGE_TAG=latest
EOF

chmod 600 "$ENV_FILE"

# в”Ђв”Ђ Summary в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo ""
echo -e "${GREEN}${BOLD}вњ“ .env created successfully!${RESET}"
echo ""
echo -e "${BOLD}в•ђв•ђв•ђв•ђ MCP server (Claude AI tools) в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ${RESET}"
echo -e "Add to Claude Desktop / Claude Code config:"
echo ""
echo -e "  ${CYAN}{${RESET}"
echo -e "  ${CYAN}  \"mcpServers\": {${RESET}"
echo -e "  ${CYAN}    \"craftcontrol\": {${RESET}"
echo -e "  ${CYAN}      \"transport\": \"sse\",${RESET}"
echo -e "  ${CYAN}      \"url\": \"http://$(hostname -I | awk '{print $1}'):${MCP_PORT}/sse\",${RESET}"
echo -e "  ${CYAN}      \"headers\": { \"Authorization\": \"Bearer ${MCP_AUTH_TOKEN}\" }${RESET}"
echo -e "  ${CYAN}    }${RESET}"
echo -e "  ${CYAN}  }${RESET}"
echo -e "  ${CYAN}}${RESET}"
echo ""
echo -e "${BOLD}в•ђв•ђв•ђв•ђ Next step в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ${RESET}"
echo -e "  Run: ${BOLD}make deploy${RESET}"
